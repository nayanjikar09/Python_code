
def add(n1, n2):
    return n1 + n2

def sub(n1, n2):
    return n1 - n2

def div(n1, n2):
    return n1 // n2 

def divi(n1, n2):
    return n1 / n2  # Floating-point division

def divis(n1, n2):
    return n1 % n2  # Modulus

def sq(num):
    return num ** 2

def sqr(num):
    return num ** 0.5

def cube(num):
    return num ** 3

def curt(num):
    return num ** (1/3)

def CA(r):
    return 3.14 * r ** 2  # Circle area

def SA(s):
    return s ** s  # Unusual; double-check logic

def RA(l, b):
    return l * b  # Rectangle area

def TA(base, height):
    return 0.5 * base * height  # Triangle area
